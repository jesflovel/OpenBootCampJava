package ejercicio;

public class SmartDevice {

    //1.Atributos
    protected String marca;

    protected String modelo;

    protected double precio;

    protected double tamanioPnatallaPulgadas;

    //2. constructores
    public SmartDevice(){

    }

    public SmartDevice(String marca, String modelo, double precio, double tamanioPnatallaPulgadas) {
        this.marca = marca;
        this.modelo = modelo;
        this.precio = precio;
        this.tamanioPnatallaPulgadas = tamanioPnatallaPulgadas;
    }

    //3.Metodos

}
